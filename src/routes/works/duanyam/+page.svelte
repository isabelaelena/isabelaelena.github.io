        <h2 class="font-bold">PRODUCT DESIGN PROJECTS</h2>

        <h2 class="font-bold">WHOLESALE (CORPORATE SOUVENIRS)</h2>
    
        <p>
            Corporate souvenirs are used as gifts or for wholesale to B2B companies. These products need to look professional while remaining affordable. After several iterations, mockups, and refinements, the approved designs were the card holder and the car registration (STNK) holders.
        </p>
    
        <h2 class="font-bold">PACKAGING DESIGN</h2>
        <p>
            For the annual IMF (International Monetary Fund) Conference in Bali, the organizers wanted to offer Indonesian traditional cloth (kain) as a souvenir, as the attendees were from all over the world. I was tasked with designing packaging that was both unisex and professional. I proposed three designs, and design #2 was chosen.
        </p>
    
        <h2 class="font-bold">STARBUCKS MERCHANDISE</h2>
    
        <p>
            A Starbucks Reserve was opening in Dewata, Bali, and Du’Anyam aimed to create limited edition merchandise for the occasion. Unfortunately, my internship ended just before the decision was made on whether to proceed with manufacturing or not.
