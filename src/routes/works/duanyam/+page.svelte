    <h2 class="font-bold pt-4">PACKAGING DESIGN</h2>
    <p class="pt-4">
        For the annual IMF (International Monetary Fund) Conference in Bali, the organizers wanted to offer Indonesian traditional cloth (kain) as a souvenir, as the attendees were from all over the world. I was tasked with designing packaging that was both unisex and professional. I proposed three designs, and design #2 was chosen.
